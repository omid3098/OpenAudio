using OpenAudio;
using UnityEngine;

public class AudioSampleUsage : MonoBehaviour
{
    void Update()
    {
        if (Input.GetKeyDown(KeyCode.Q))
        {
            AudioManager.instance.Play(OpenAudio.AudioType.audio1, true);
        }
        if (Input.GetKeyDown(KeyCode.W))
        {
            AudioManager.instance.Play(OpenAudio.AudioType.audio2);
        }
    }
}
